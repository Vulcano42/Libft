#include <stdio.h>
#include <stdlib.h>

char	*ft_strjoin(const char *s1, const char *s2)

{
	char	*str;
	size_t	i;
	size_t	j;

	if (!s1 || !s2)
		return (NULL);
	i = ft_strlen(s1);
	j = ft_strlen(s2);
	str = malloc(sizeof(char) * (i + j + 1));
	if (!str)
		return (NULL);
	ft_strlcpy(str, s1, i +1);
	ft_strlcat(str, s2, i + j + 1);
	return (str);
}
